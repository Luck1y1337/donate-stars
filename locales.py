DEFAULT_LANG = "ru"


def get_text(lang, key, **kwargs):
    """Возвращает текст по ключу для нужного языка с подстановкой значений."""
    if lang not in TEXTS:
        lang = DEFAULT_LANG
    if key in TEXTS[lang]:
        text = TEXTS[lang][key]
    else:
        text = TEXTS[DEFAULT_LANG][key]
    if kwargs:
        text = text.format(**kwargs)
    return text


def get_all_translations(key):
    """Возвращает список переводов ключа на всех языках (для фильтров кнопок)."""
    translations = []
    for lang in TEXTS:
        if key in TEXTS[lang]:
            translations.append(TEXTS[lang][key])
    return translations
